			lucro, que proclama, practica y enseña el
			evangelio de fe y poder, formando discípulos
			para Nuestro Señor Jesucristo, quien es el
			único mediador entre Dios y los hombres, con
			el fin de bendecir a Colombia y a las naciones.
   </p>
</div>

<div class=" col-md-5 bg-secondary text-white offset-0 mt-3 m-5">
  <h5 class="card-header bg-primary2 text-center col-md-12">
    Visión
  </h5>
  <p class="card-body  text-justify col-md-12">
    	<br>
    	Desarrollar un avivamiento espiritual
		permanente que genere crecimiento integral
		en las personas para la gloria de Dios.
    
   </p>
</div>

</div>

<h4 class="col-md-12 text-center mt-5">Nuestros principios y valores.</h4>
	<hr class="col-md-1">
	<div class="col-md-12 mt-3"></div>

 <div class=" col-md-5 bg-secondary text-white  m-5">
  <div class="card-header bg-primary2 text-center">
    <h5>Amor</h5>
  </div>
  <div class="card-body m-4 text-justify">
    	<br>
    	El amor es la esencia de Dios y refleja su atributo
		y carácter, “El que no ama, no ha conocido a
		Dios, porque Dios es amor” (1 Juan 4:8).
		Después de Dios, nuestra mayor riqueza son las
		personas. Promovemos y contribuimos al
		desarrollo integral del ser humano; dando en
		todo la gloria a DIOS PADRE, HIJO Y ESPÍRITU
		SANTO. El Padre nos ama y demanda amor, de
		igual manera Jesús ordena amarnos los unos a
		los otros.
		(Marcos 12:30-31).
   </div>
</div>	

<div class=" col-md-5 bg-secondary text-white  m-5">
  <div class="card-header bg-primary2 text-center">
    <h5>Obediencia</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <br>
    	Es redimir enteramente nuestra voluntad a la
		voluntad de Dios y su palabra, siendo imitadores
		de Jesús quien fue obediente hasta la muerte y
		muerte de cruz, (Juan 6:38; Filipenses 2:8);
		recordando que la obediencia refleja el amor
		hacia Dios y trae bendiciones (Juan 14:21-24).
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Sacrificio</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	La escritura enuncia que debemos presentar todo
		nuestro ser, espíritu, alma y cuerpo “en sacrificio
		vivo, santo y agradable a Dios”; aun en medio de
		las pruebas, persecuciones o escasez (Romanos
		12:1; Mateo 16:24).
    </p>
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Santidad</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	La santidad es una expresión de la consagración
		a Dios y separación de los placeres humanos. Se
		puede evidenciar en santidad posicional, cuando
		nacemos de nuevo; experimental, en el desarrollo
		cristiano y diario vivir en comunión con Dios; y
		eterna, cuando estemos con el Señor en el cielo y
		no tengamos nada que ver con el pecado,
		(Levítico 20:26; Efesios 1:4).
    </p>
   </div>
</div>

<div class=" col-md-11 bg-secondary text-white  m-5  mt-3">
  <div class="card-header bg-primary2 text-center">
    <h5>Fe</h5>
  </div>
  <div class="card-body m-4 ">
    La Fe tiene dos partes importantes:
    <hr class="col-md-12">
    <p class="">
    	<br>
    	La objetiva.
    	<hr class="col-md-1"> 
    	Es creer en el sacrificio de Cristo,
		hecho por toda la humanidad y fe en la palabra de
		Dios como la revelación divina de la voluntad de
		Dios a favor de los hombres, (Efesios 4:5).
		<br>
		<br>
		La subjetiva.
		<hr class="col-md-1">
		Es la seguridad y la convicción
		interior que nos lleva a confiar en Dios y su
		palabra. Por lo cual vivimos por fe y para fe,
		(Romanos 1:7; Hebreos 11.1) “Es pues la fe la
		certeza de los que se espera, la convicción de lo
		que no se ve”.
    </p>
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Unidad</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	La unidad es una exigencia de Cristo para su
		pueblo. Como Iglesia del Señor, somos un cuerpo
		en Cristo, aunque con diferentes dones y
		ministerios. Estamos llamados a guardar la
		unidad en medio de la diversidad.
		(Juan 17:21-23; Colosenses 3:11).
    </p>
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Integridad</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	Somos llamados a ser íntegros; entendiéndose
		por integridad, ser una persona que muestra
		congruencia entre lo que dice. Que es
		transparente, honrada y justa; de un solo criterio y
		que lleva una vida íntima con Dios, honesta y
		recta con sus semejantes en su comportamiento,
		sincera en todo. (Salmos 15:2 y 101:2; tito 2:7).
    </p>
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Generosidad</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	Como hijos de Dios nos corresponde practicar la
		generosidad que es un acto de dar
		desinteresadamente para ayudar a otros, sin
		esperar nada a cambio. Es un principio de
		siembra y cosecha que trae muchas y grandes
		bendiciones a quien lo hace. (Salmos 41:1-13;
		Proverbios 19:17).
    </p>
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Compromiso</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	El compromiso es un acuerdo voluntario; pacto o
		convenio que se hace entre dos o más personas
		o entidades, sujeto a cumplimiento entre ellas.
		Tenemos compromiso con Dios, la familia, la
		iglesia y la sociedad, dar a conocer las verdades
		del evangelio, llevando una vida dedicada a Dios.
		(1Corintios 9:16b).
		    </p>
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Verdad</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	La verdad es la conformidad de las cosas o el
		pensamiento con la realidad. Además de ser un
		concepto, la verdad fundamentalmente es una
		persona, Jesucristo nuestro Señor.
		En un mundo que se ejercita en la mentira, es
		veraz todo aquel que vive y profesa la verdad. El
		cristiano debe siempre optar por la práctica y
		vivencia de la verdad. (Efesios 4:25; Zacarías
		8:16).
		    </p>
   </div>
</div>

<div class=" col-md-5 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Humildad</h5>
  </div>
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	La humildad se muestra en el servicio a Dios y a
		nuestros semejantes, haciéndolo con amor,
		alegría y gozo, siguiendo el ejemplo de nuestro
		Señor Jesucristo, quien siendo Señor se hizo
		siervo, sin esperar nada a cambio. (Mateo 20:25-
		28; Hechos 20:19; Proverbios 22:4).
    </p>
   </div>
</div>

<div class=" col-md-11 bg-secondary text-white  m-5 mt-2">
  <div class="card-header bg-primary2 text-center">
    <h5>Lealtad</h5>
  </div>
  <div class="card-body m-4 text-center">
    <p>
    	<br>
    	Es la fidelidad primeramente a Dios, luego a la
		familia, iglesia e institución en la cual servimos,
		(Jeremías 42:5).
    </p>
   </div>
</div>

<h3 class="col-md-12 text-center mt-5">Nuestras declaraciones de fe.</h3>
	<hr class="col-md-1">
	<div class="col-md-12 mt-3"></div>

	<div class=" col-md-10 bg-secondary text-white  ml-10 mt-2">
  <div class="card-body m-4 text-justify">
    <p>
    	<br>
    	1. Creemos en la plena inspiración por el
		Espíritu Santo de las Sagradas Escrituras
		como regla única e infalible de fe y      conducta.
		<br>
		<br>
		2. Creemos en el Eterno Dios Padre, Dios
		Hijo, y Dios Espíritu Santo; Dios en tres
		Personas.
        <br>
        <br>
		3. Creemos en la plenitud de la Deidad y
		Realeza de nuestro Señor Jesucristo y su
		verdadera humanidad impecable.
		<br>
		<br>
		4. Creemos que el Señor Jesucristo fue
		engendrado por el Espíritu Santo, siendo
		María virgen.
		<br>
		<br>
		5. Creemos en la muerte sustitutiva y
		expiatoria de nuestro Señor Jesucristo en la
		Cruz.
		<br>
		<br>
		6. Creemos que el Señor Jesucristo resucitó
		de entre los muertos con un cuerpo
		glorificado y que volverá en poder y gran
		gloria.
		<br>
		<br>
		7. Creemos en la salvación por gracia
		soberana de Dios, alcanzada por medio de la
		fe en nuestro Señor Jesucristo.
        <br>
        <br>
		8. Creemos que solamente hay dos lugares
		como destino eterno del ser humano: El Cielo
		para los redimidos por el Señor Jesucristo y
		el infierno para los que lo rechacen.
		<br>
		<br>
		9. Creemos que la regeneración del ser
		humano es por el Espíritu Santo y la Palabra
		de Dios.
		<br>
		<br>
		10. Creemos en la unidad espiritual de todos
		los redimidos por la Sangre del Señor
		Jesucristo.
		<br>
		<br>
		11. Creemos en la necesidad de mantener
		conforme al Evangelio, la pureza de la Iglesia
		en doctrina y conducta.
		<br>
		<br>
		12. Creemos que la iglesia de Dios es el
		conglomerado de los salvos por nuestro
		Señor Jesucristo y regenerados por el
		Espíritu Santo.
    </p>
   </div>
